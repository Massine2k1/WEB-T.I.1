        <p>L'influence lacustre se retrouve dans l'étymologie même de Berlin, issue d’une racine slave *brl qui désigne un marais ou une zone marécageuse. Le toponyme Berlin pourrait être aussi rapproché du terme sorabe barlen ou berlén qui désigne les grillages de bois placés en divers endroits d'une rivière par les pêcheurs. Contrairement à ce que l'étymologie populaire affirme, Berlin n’est pas basé sur le nom allemand de l'ours, Bär, suivi du suffixe diminutif -lin sous une forme dialectale (allemand -lein). D'ailleurs, l'accentuation même s'oppose à cette interprétation, puisque l'on doit prononcer [bɛɐ̯.ˈliːn] en appuyant sur le i long, tout comme dans les nombreux toponymes en -in de la partie est de l'Allemagne (ex. : Schwerin) qui remontent au slave. L'ours a néanmoins été choisi pour être la mascotte de la ville et figure sur ses armoiries.</p>

        <p>On distingue le plateau de Barnim au nord, le plateau de Teltow au sud et le plateau de Nauen (Nauener Platte) au sud-ouest, ainsi que la vallée proglaciaire de Varsovie-Berlin (Berliner Urstromtal) transversale qui suit le cours de la Sprée.
            La ville de Berlin se situe dans le Nord-Est de l’Allemagne, dans la plaine germano-polonaise, à 33 m d'altitude, au confluent de la Sprée et de la Havel. Les affluents de la Sprée sont la Panke, la Dahme, la Wuhle et l'Erpe. Une particularité de la ville est la présence de nombreux lacs et rivières, le long des cours d'eau. On en trouve plusieurs à l'ouest, mais aussi à l'est avec le Müggelsee.</p>

        <p>La topographie berlinoise a été fortement influencée par la dernière ère glaciaire, et par la glaciation saalienne et la glaciation vistulienne qui a dessiné la vallée proglaciaire de Varsovie-Berlin qui traverse le land d'est en ouest entre le plateau de Barnim au nord et le plateau de Teltow au sud.</p>

        <p>La capitale allemande possède un climat semi-continental, caractérisé par une amplitude plus importante qu'en climat océanique (plus à l'ouest) entre des hivers modérément froids et relativement secs et des étés assez chauds et orageux. Les précipitations tombent d'ailleurs majoritairement durant la saison estivale. Le record de chaleur est de 38,1 °C le 11 juillet 1959 et le record de froid de −26 °C le 11 février 1929. La température moyenne annuelle est de 9,7 °C.</p>
